private void inserirProduto() {

        String nome = txtNome.getText();
        Double preco = Double.valueOf(txtPreco.getText());
        Produto pd = new Produto(nome, preco);
        ProdutoDB mysql = new ProdutoDB();

        try {
            mysql.insertProduto(pd);
        } catch (Exception ex) {
            System.out.println("erro no view para inserir produto");
            throw new RuntimeException(ex);
        }

    }

 private void listarValores() {

        try {

            ProdutoDB produto = new ProdutoDB();
            DefaultTableModel model = (DefaultTableModel) tabelaProduto.getModel();
            model.setNumRows(0);

            String filtro = txtId.getText();
            System.out.println(filtro);

            ArrayList<Produto> resultado = (ArrayList<Produto>) produto.selectProduto();
            for (int num = 0; num < resultado.size(); num++) {

                model.addRow(new Object[] {

                        resultado.get(num).getIdProduto(),
                        resultado.get(num).getNomeProduto(),
                        resultado.get(num).getPrecoProduto()
                });
            }

        } catch (Exception e) {
            System.out.println("erro");
            throw new RuntimeException(e);

        }
    }
 
 
 private void listarValoresFiltro() {

        try {

            ProdutoDB produto = new ProdutoDB();
            DefaultTableModel model = (DefaultTableModel) tabelaProduto.getModel();
            model.setNumRows(0);
            String filtro = txtFiltro.getText();
            
            ArrayList<Produto> resultado = (ArrayList<Produto>) produto.selectProduto("Where idProduto = "+filtro);

            for (int num = 0; num < resultado.size(); num++) {

                model.addRow(new Object[] {

                        resultado.get(num).getIdProduto(),
                        resultado.get(num).getNomeProduto(),
                        resultado.get(num).getPrecoProduto()
                });
            }

        } catch (Exception e) {
            System.out.println("erro");
            throw new RuntimeException(e);

        }
    }
 
  private void CarregarProduto() {

        int setar = tabelaProduto.getSelectedRow();

        txtId.setText(tabelaProduto.getModel().getValueAt(setar, 0).toString());
        txtNome.setText(tabelaProduto.getModel().getValueAt(setar, 1).toString());
        txtPreco.setText(tabelaProduto.getModel().getValueAt(setar, 2).toString());

    }
  
  private void deletarProduto() {

        int idProduto;

        idProduto = Integer.parseInt(txtId.getText());

        Produto produto = new Produto(idProduto);

        ProdutoDB mysql = new ProdutoDB();

        try {
            mysql.deleteProduto(produto);
        } catch (Exception ex) {
            System.out.println("erro no view para excluir produto");
            throw new RuntimeException(ex);
        }

    }
  
  public void apagaCampos(){
      
      txtId.setText("");
      txtNome.setText("");
      txtPreco.setText("");
      
  }
  
  private void AlterarProduto() {

        int idProduto;
        String nomeProduto;
        double precoProduto;

        idProduto = Integer.parseInt(txtId.getText());
        nomeProduto = txtNome.getText();
        precoProduto = Double.valueOf(txtPreco.getText());

        Produto produto = new Produto(idProduto, nomeProduto, precoProduto);

        ProdutoDB mysql = new ProdutoDB();

        try {
            mysql.updateProduto(produto);
        } catch (Exception ex) {
            System.out.println("erro no view para alterar produto");
            throw new RuntimeException(ex);
        }

    }


